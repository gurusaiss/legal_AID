function wrap(title: string, body: string) {
  return `<!DOCTYPE html><html lang="en"><head><meta charset="utf-8"/>
<title>${title}</title><style>${CSS}</style></head><body>${body}
<div class="footer">Generated by <strong>LegalAID</strong> — Free Legal AI for Tribal &amp; Rural Communities |
National Legal Services Helpline: <strong>15100</strong> (Toll-Free, All India) |
This document is for informational purposes. Consult a qualified advocate or DLSA for legal advice.
</div></body></html>`;
}

function addr(s: string) {
  return s.replace(/\n/g, "<br/>");
}

