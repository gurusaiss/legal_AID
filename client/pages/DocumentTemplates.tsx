// ─── Component ────────────────────────────────────────────────────────────────

export default function DocumentTemplates() {
  const [selectedId, setSelectedId] = useState<string | null>(null);
  const [values, setValues] = useState<Record<string, string>>({});

  const template = TEMPLATES.find((t) => t.id === selectedId) ?? null;

  const handleSelect = useCallback((id: string) => {
    setSelectedId(id);
    setValues({});
  }, []);

  const handleChange = useCallback((id: string, val: string) => {
    setValues((prev) => ({ ...prev, [id]: val }));
  }, []);

  const handlePrint = useCallback(() => {
    if (!template) return;
    const html = template.generate(values);
    const win = window.open("", "_blank");
    if (!win) {
      alert("Please allow pop-ups to print the document.");
      return;
    }
    win.document.write(html);
    win.document.close();
    // Give the browser a moment to render before triggering print
    setTimeout(() => win.print(), 400);
  }, [template, values]);

  const filledCount = template
    ? template.fields.filter((f) => f.required && values[f.id]?.trim()).length
    : 0;
  const requiredCount = template ? template.fields.filter((f) => f.required).length : 0;
  const isReady = filledCount === requiredCount;

  return (
    <Layout>
      <div className="mx-auto max-w-4xl space-y-6">
        {/* Header */}
        <div>
          <h1 className="text-2xl font-bold text-slate-900 flex items-center gap-2">
            <FileText className="w-6 h-6 text-blue-600" />
            Legal Document Templates
          </h1>
          <p className="text-slate-500 text-sm mt-1">
            Fill in your details, preview the document, and print or save as PDF — free, no sign-up needed.
          </p>
        </div>

        {/* Template selector */}
        <div className="grid gap-3 sm:grid-cols-2 lg:grid-cols-4">
          {TEMPLATES.map((t) => (
            <button
              key={t.id}
              onClick={() => handleSelect(t.id)}
              className={`rounded-xl border p-4 text-left transition-all hover:shadow-md ${
                selectedId === t.id
                  ? "border-blue-500 bg-blue-50 shadow-md ring-2 ring-blue-200"
                  : "border-slate-200 bg-white hover:border-slate-300"
              }`}
            >
              <div className={`inline-flex rounded-lg p-2 mb-2 ${t.iconBg}`}>
                <t.Icon className={`w-5 h-5 ${t.iconColor}`} />
              </div>
              <div className="font-semibold text-slate-900 text-sm">{t.title}</div>
              <div className="text-xs text-slate-500 mt-0.5">{t.subtitle}</div>
            </button>
          ))}
        </div>

        {/* Form + Live preview side by side */}
        {template && (
          <div className="grid gap-6 lg:grid-cols-2">
            {/* Form */}
            <div className="rounded-2xl bg-white shadow-sm ring-1 ring-slate-200 p-5 space-y-4">
              <div className="flex items-center gap-2">
                <button
                  onClick={() => setSelectedId(null)}
                  className="text-slate-400 hover:text-slate-700 transition-colors"
                  title="Back to templates"
                >
                  <ChevronLeft className="w-5 h-5" />
                </button>
                <div>
                  <h2 className="font-bold text-slate-900">{template.title}</h2>
                  <p className="text-xs text-slate-500">{template.law}</p>
                </div>
              </div>

              <div className="space-y-3">
                {template.fields.map((field) => (
                  <div key={field.id} className="space-y-1">
                    <label className="text-sm font-medium text-slate-700">
                      {field.label}
                      {field.required && <span className="text-red-500 ml-0.5">*</span>}
                    </label>
                    {field.type === "textarea" ? (
                      <textarea
                        rows={3}
                        value={values[field.id] ?? ""}
                        onChange={(e) => handleChange(field.id, e.target.value)}
                        placeholder={field.placeholder}
                        className="w-full rounded-lg border border-slate-200 bg-slate-50 px-3 py-2 text-sm outline-none focus:border-blue-400 focus:ring-2 focus:ring-blue-100 resize-y"
                      />
                    ) : field.type === "select" ? (
                      <select
                        value={values[field.id] ?? ""}
                        onChange={(e) => handleChange(field.id, e.target.value)}
                        className="w-full rounded-lg border border-slate-200 bg-slate-50 px-3 py-2 text-sm outline-none focus:border-blue-400 focus:ring-2 focus:ring-blue-100"
                      >
                        <option value="">— Select —</option>
                        {field.options?.map((o) => <option key={o}>{o}</option>)}
                      </select>
                    ) : (
                      <Input
                        type={field.type}
                        value={values[field.id] ?? ""}
                        onChange={(e) => handleChange(field.id, e.target.value)}
                        placeholder={field.placeholder}
                        className="bg-slate-50"
                      />
                    )}
                    {field.hint && (
                      <p className="text-xs text-slate-400">{field.hint}</p>
                    )}
                  </div>
                ))}
              </div>

              {/* Progress + Print */}
              <div className="pt-2 space-y-3">
                <div className="flex items-center gap-2 text-xs text-slate-500">
                  <div className="flex-1 rounded-full bg-slate-100 h-1.5">
                    <div
                      className="rounded-full bg-blue-500 h-1.5 transition-all"
                      style={{ width: `${requiredCount ? (filledCount / requiredCount) * 100 : 0}%` }}
                    />
                  </div>
                  <span>{filledCount}/{requiredCount} required fields</span>
                </div>
                <Button
                  onClick={handlePrint}
                  disabled={!isReady}
                  className="w-full bg-blue-600 hover:bg-blue-700 gap-2"
                >
                  <Printer className="w-4 h-4" />
                  {isReady ? "Print / Save as PDF" : "Fill required fields to print"}
                </Button>
                <p className="text-xs text-center text-slate-400">
                  In the print dialog, choose "Save as PDF" to get a file.
                </p>
              </div>
            </div>

            {/* Live preview */}
            <div className="rounded-2xl bg-white shadow-sm ring-1 ring-slate-200 overflow-hidden flex flex-col">
              <div className="bg-slate-100 px-4 py-2 flex items-center gap-2 border-b border-slate-200">
                <div className="flex gap-1.5">
                  <div className="w-3 h-3 rounded-full bg-red-400" />
                  <div className="w-3 h-3 rounded-full bg-yellow-400" />
                  <div className="w-3 h-3 rounded-full bg-green-400" />
                </div>
                <span className="text-xs text-slate-500 font-medium">Document Preview</span>
              </div>
              <div className="flex-1 overflow-y-auto p-5 font-serif text-[11px] leading-relaxed text-slate-800 space-y-2">
                <DocumentPreview template={template} values={values} />
              </div>
            </div>
          </div>
        )}

        {/* Bottom tip */}
        {!template && (
          <div className="rounded-xl bg-blue-50 border border-blue-100 p-4 text-sm text-blue-800">
            <strong>How it works:</strong> Select a template above → fill in your details →
            preview updates live → click "Print / Save as PDF" to get your document.
            All documents are formatted as proper legal letters citing the relevant Indian law.
          </div>
        )}
      </div>
    </Layout>
  );
}

